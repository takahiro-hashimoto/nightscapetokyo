// ─── メイン処理 ───────────────────────────────────────────────
async function main() {
  // 1. tokyo-sky-tree タグページを取得
  const { data: tagPage, error: tpErr } = await supabase
    .from("tag_pages")
    .select("id, tag:tags(slug)")
    .eq("tag_id", (
      await supabase.from("tags").select("id").eq("slug", "tokyo-sky-tree").single()
    ).data.id)
    .single();

  if (tpErr || !tagPage) {
    console.error("tag_page が見つかりません:", tpErr);
    process.exit(1);
  }
  console.log(`✅ tag_page id: ${tagPage.id}`);

  const pageId = tagPage.id;

  // 2. 全スポットを slug で取得
  const allSlugs = SECTIONS.flatMap((s) => s.spotSlugs);
  const { data: dbSpots, error: spotErr } = await supabase
    .from("spots")
    .select("id, slug, name, title")
    .in("slug", allSlugs);

  if (spotErr) {
    console.error("スポット取得エラー:", spotErr);
    process.exit(1);
  }

  const spotBySlug = new Map((dbSpots ?? []).map((s) => [s.slug, s]));

  // 見つからなかったスラッグを報告
  const missing = allSlugs.filter((sl) => !spotBySlug.has(sl));
  if (missing.length > 0) {
    console.warn(`⚠️  DB にないスラッグ (${missing.length}件):`, missing);
  }

  // 3. 既存セクションを削除（cascade で section_spots も削除される）
  const { error: delErr } = await supabase
    .from("tag_page_sections")
    .delete()
    .eq("tag_page_id", pageId);

  if (delErr) {
    console.error("セクション削除エラー:", delErr);
    process.exit(1);
  }
  console.log("✅ 旧セクション削除完了");

  // 4. セクションを再挿入
  for (let si = 0; si < SECTIONS.length; si++) {
    const sec = SECTIONS[si];

    const { data: section, error: secErr } = await supabase
      .from("tag_page_sections")
      .insert({
        tag_page_id: pageId,
        key: sec.key,
        heading: sec.heading,
        intro: sec.intro,
        sort_order: si,
      })
      .select("id")
      .single();

    if (secErr || !section) {
      console.error(`セクション "${sec.heading}" 作成エラー:`, secErr);
      continue;
    }

    // 5. スポットを挿入
    const spotRows = [];
    for (let i = 0; i < sec.spotSlugs.length; i++) {
      const slug = sec.spotSlugs[i];
      const spot = spotBySlug.get(slug);
      if (!spot) {
        console.warn(`  ⚠️  スキップ: ${slug} (DBに存在しない)`);
        continue;
      }
      spotRows.push({
        section_id: section.id,
        spot_id: spot.id,
        description: DESCRIPTIONS[slug] ?? null,
        sort_order: i,
      });
    }

    if (spotRows.length > 0) {
      const { error: spotsErr } = await supabase
        .from("tag_page_section_spots")
        .insert(spotRows);

      if (spotsErr) {
        console.error(`  スポット挿入エラー (${sec.heading}):`, spotsErr);
      } else {
        console.log(`  ✅ ${sec.heading}: ${spotRows.length}件挿入`);
      }
    }
  }

  console.log("\n✅ 完了");
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
